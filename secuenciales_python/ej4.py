# 4. Dados dos números, mostrar la suma, resta, división y multiplicación de ambos.

num1 = int(input("Imprime el primer número que quieres hacerle la operación: "))
num2 = int(input("Imprime el segundo número que quieres hacerle la operación: "))

suma = num1 + num2
resta = num1 - num2
mult = num1 * num2
div = num1 / num2

print('La suma de los números es de: ', suma)
print('La resta de los números es de: ', resta)
print('La multiplicación de los números es de: ', mult)
print('La división de los números es de: ', div)
